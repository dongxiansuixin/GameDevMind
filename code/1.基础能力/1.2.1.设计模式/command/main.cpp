// ============================================================
// main.cpp — 命令模式演示：游戏输入录制 → 执行 → 撤销 → 回放
// ============================================================
// 场景模拟：
//   玩家"勇者"在 2D 地图上输入一系列操作：
//     向右移动 → 向下移动 → 攻击 → 释放火球术
//   系统录制这些命令，然后依次执行、撤销部分操作、最后回放。
// ============================================================

#include "command.hpp"
#include "command_queue.hpp"
#include <iostream>

int main() {
    std::cout << "╔══════════════════════════════════════════════╗\n";
    std::cout << "║   命令模式演示：游戏输入录制/执行/撤销/回放 ║\n";
    std::cout << "╚══════════════════════════════════════════════╝\n";

    // -------------------------------------------------------
    // 1. 创建玩家
    // -------------------------------------------------------
    Player player{"勇者"};
    std::cout << "\n🎮 初始状态：";
    player.Print();

    // -------------------------------------------------------
    // 2. 录制玩家输入序列
    // -------------------------------------------------------
    CommandQueue queue;

    std::cout << "\n📝 录制玩家输入序列...\n";
    queue.Record(std::make_unique<MoveCommand>(1, 0));   // 向右移动
    queue.Record(std::make_unique<MoveCommand>(0, 1));   // 向下移动
    queue.Record(std::make_unique<AttackCommand>(15));   // 普通攻击
    queue.Record(std::make_unique<SkillCommand>(
        "Fireball",
        // 执行：火球术让玩家向前冲刺
        [](Player& p) { p.pos_x += 10; std::cout << "    🔥 火球术！向前冲刺 10 单位\n"; },
        // 撤销：回退冲刺
        [](Player& p) { p.pos_x -= 10; std::cout << "    💨 撤销火球术冲刺\n"; }
    ));
    queue.Record(std::make_unique<MoveCommand>(5, 0));   // 向前跑

    queue.PrintRecorded();

    // -------------------------------------------------------
    // 3. 批量执行所有命令
    // -------------------------------------------------------
    queue.ExecuteAll(player);
    std::cout << "\n✅ 执行后状态：";
    player.Print();

    // -------------------------------------------------------
    // 4. 撤销最近 2 条命令（撤销"向前跑"和"火球术"）
    // -------------------------------------------------------
    queue.UndoLast(player, 2);
    std::cout << "\n↩️  撤销 2 步后状态：";
    player.Print();

    // -------------------------------------------------------
    // 5. 重做 1 条命令（重做"火球术"）
    // -------------------------------------------------------
    queue.RedoLast(player, 1);
    std::cout << "\n↪️  重做 1 步后状态：";
    player.Print();

    // -------------------------------------------------------
    // 6. 完整回放：重置玩家，从录制序列重新执行
    // -------------------------------------------------------
    std::cout << "\n🔄 === 完整回放模式 ===";
    queue.Replay(player);
    std::cout << "\n📺 回放后状态：";
    player.Print();

    std::cout << "\n════════════════ 演示结束 ════════════════\n";
    return 0;
}
